/**
 * Check if a word is a valid 5-letter English word
 *
 * @param word - The word to validate (case-insensitive)
 * @returns true if the word is in the valid words dictionary
 *
 * @example
 * isValidWord('HELLO') // true
 * isValidWord('hello') // true
 * isValidWord('XYZAB') // false
 * isValidWord('HI')    // false (not 5 letters)
 */
export function isValidWord(word: string): boolean {
  if (!word || word.length !== 5) {
    return false;
  }
  return VALID_FIVE_LETTER_WORDS.has(word.toUpperCase());
}
